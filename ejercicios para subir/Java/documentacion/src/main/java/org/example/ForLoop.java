package org.example;

public class ForLoop {
    public static void main(String[] args) {


        for (int i = 0; i < 10; i++){  //siempre arranca en cero, asi que el numero final va a ser 9
            System.out.println(i + " Prueba para imprimir 10 veces lo mismo");
        }
        System.out.println("Fin");
    }
}
